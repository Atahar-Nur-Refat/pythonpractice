thislist = ["apple", "banana", "cherry"]
mylist = thislist.copy()#copy a list
print(mylist)
#Make a copy of a list with the list() method:
thislist = ["apple", "banana", "cherry"]
mylist = list(thislist)#it can also be used
print(mylist)
#also this can be used Make a copy of a list with the : operator
thislist = ["apple", "banana", "cherry"]
mylist = thislist[:]# : it is called the slice operator
print(mylist)